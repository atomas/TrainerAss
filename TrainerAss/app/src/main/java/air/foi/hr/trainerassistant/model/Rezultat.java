package air.foi.hr.trainerassistant.model;

public class Rezultat {

    private String disciplina, prvi, drugi, treci;

    public String getDisciplina() {
        return disciplina;
    }

    public void setDisciplina(String disciplina) {
        this.disciplina = disciplina;
    }

    public String getPrvi() {
        return prvi;
    }

    public void setPrvi(String prvi) {
        this.prvi = prvi;
    }

    public String getDrugi() {
        return drugi;
    }

    public void setDrugi(String drugi) {
        this.drugi = drugi;
    }

    public String getTreci() {
        return treci;
    }

    public void setTreci(String treci) {
        this.treci = treci;
    }
}
